/**
*  是否 little_endian序.
* 1) Little-Endian就是低位字节排放在内存的低地址端，高位字节排放在内存的高地址端。
* 2) Big-Endian就是高位字节排放在内存的低地址端，低位字节排放在内存的高地址端
*  @retval { bool } little_endian : true , Big-Endian : false
*  @version     07/25/2008  shijunpu  Initial Version.
*/
inline bool is_little_endian()
{
	const int i = 1;
	char *ch = (char*)&i;
	return ch[0] && 0x01;
}

/**
*  字节顺序转换.
*
*  @param  -[in, out]  char*  pOrig: [comment]
*  @param  -[in]  int  nLength: [comment]
*  @param  -[in]  int  nWordBytes: [comment]
*  @retval { long }  success : 0
*  @version     07/25/2008  shijunpu  Initial Version.
*/
inline long SwapByteOrder(char* pOrig, int nLength, int nWordBytes)
{
	int i = 0;
	int nSwapCount = 0;
	char chTemp;

	if (nWordBytes == 2)
	{
		nSwapCount = nLength / 2;
		for (i = 0; i < nSwapCount; i++)
		{
			// 第0和第1个字节交换
			chTemp = *pOrig;
			*pOrig = *(pOrig + 1);
			*(pOrig + 1) = chTemp;
			pOrig += 2;
		}
	}
	else if (nWordBytes == 4)
	{
		nSwapCount = nLength / 4;
		for (i = 0; i < nSwapCount; i++)
		{
			// 第0和第3个字节交换
			chTemp = *(pOrig + 3);
			*(pOrig + 3) = *pOrig;
			*pOrig = chTemp;

			// 第1和第2个字节交换
			chTemp = *(pOrig + 2);
			*(pOrig + 2) = *(pOrig + 1);
			*(pOrig + 1) = chTemp;

			pOrig += 4;
		}
		// 不足4个字节的部分
		if (nLength - nSwapCount * 4 == 2)
		{
			// 剩余两个字节
			chTemp = *pOrig;
			*pOrig = *(pOrig + 1);
			*(pOrig + 1) = chTemp;
		}
		else if (nLength - nSwapCount * 4 == 3)
		{
			// 剩余三个字节
			chTemp = *pOrig;
			*pOrig = *(pOrig + 2);
			*(pOrig + 2) = chTemp;
		}
	}
	return PK_SUCCESS;
}

/**
*  设备地址转换为协议地址. 就是将头部的3 和4 去掉。并-1. 如 300001变为0， 400005变为4, 000005变为4
*  @param  -[in]  int  nAddress: [comment]
*  @retval { int } 协议地址
*  @version     07/25/2008  shijunpu  Initial Version.
*/
inline int Dev2ProtocalAddr(int nAddress)
{
	nAddress = nAddress % 100000 - 1;
	return nAddress;
}

